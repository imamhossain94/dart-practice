// Dart Numbers

void main(List<String> args) {

  int number = 100;

  print(number.isOdd);
  print(number.isEven);
  print(number.isFinite);
  print(number.isInfinite);
  print(number.isNegative);


  print(num.parse("1"));
  print(int.parse("1"));
  print(double.parse("1"));
  print(101010.toString());
  print(101010.toStringAsFixed(5));


  print(0.5.ceil());
  print(0.5.floor());
  print(0.5.round());

  print(1.compareTo(1));

}

